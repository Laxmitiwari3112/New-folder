import java.util.*;

//area of rectangle
public class AreaofRectangle {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int l = sc.nextInt();
        int b = sc.nextInt();

        int area = l * b;
        System.out.println("AREA  OF RECTANGLE:" + area);
        System.out.println();
    }

}
