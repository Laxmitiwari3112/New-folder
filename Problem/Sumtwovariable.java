import java.util.*;

public class Sumtwovariable {
    public static void main(String[] args) {
        // input by user 
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = a + b;

        //output
        System.out.println("Sum of a and b:" + sum);
    }
}
